use gpui::*;

pub fn icon(name: &str, size: Pixels, color: u32) -> Svg {
    svg()
        .path(format!("assets/icons/{name}.svg"))
        .size(size)
        .flex_shrink_0()
        .text_color(rgb(color))
}

pub fn icon_sm(name: &str, color: u32) -> Svg {
    icon(name, px(16.), color)
}

pub fn icon_md(name: &str, color: u32) -> Svg {
    icon(name, px(20.), color)
}

pub fn icon_lg(name: &str, color: u32) -> Svg {
    icon(name, px(24.), color)
}

